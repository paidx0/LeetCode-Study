class MyListNode{
public:
    int val;
    MyListNode *next;
    MyListNode(int val){
        this->val = val;
        next = nullptr;
    };
};

class MyLinkedList {
private:
    int length;
    MyListNode *head;
public:
    MyLinkedList() {
        this->length = 0;
        this->head = new MyListNode(0);
    }
    
    int get(int index) {
        if(index < 0 || index >= length) return -1;
        MyListNode *p = head->next;
        for(int i = 0; i < index; i++) p = p->next;
        return p->val;
    }
    
    void addAtHead(int val) {
        addAtIndex(0, val);
    }
    
    void addAtTail(int val) {
        addAtIndex(length, val);
    }
    
    void addAtIndex(int index, int val) {
        if(index > length) return;
        if(index < 0) index = 0;
        MyListNode *p = head;
        MyListNode *node = new MyListNode(val);
        for(int i = 0; i < index; i++) p = p->next;
        node->next = p->next;
        p->next = node;
        length++;
    }
    
    void deleteAtIndex(int index) {
        if(index < 0 || index >= length) return;
        MyListNode *p = head;
        for(int i = 0; i < index; i++) p = p->next;
        MyListNode *q = p->next;
        p->next = q->next;
        delete q;
        length--;
    }
};

